package net.linghou.erp.sellorder.domain;

/** 付款状态类 */
public class PaymentStatu {
	private int paymentStatuId;
	private String paymentStatuName;

	public int getPaymentStatuId() {
		return paymentStatuId;
	}

	public void setPaymentStatuId(int paymentStatuId) {
		this.paymentStatuId = paymentStatuId;
	}

	public String getPaymentStatuName() {
		return paymentStatuName;
	}

	public void setPaymentStatuName(String paymentStatuName) {
		this.paymentStatuName = paymentStatuName;
	}

}
