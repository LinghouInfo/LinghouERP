package net.linghou.erp.salesorder.domain;
/**退款状态*/
public class RefundStatus {
	/**状态ID*/
	private int id;
	/**状态名称*/
	private String  name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
